package DO;

public class T_Patient_DO {
	int id;
	String name;
	String age;
	String patient_room;
	String bpm;
	String body_temp;	
	String respiration;
	String blood_pressure;
	String the_others;
	int team;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getPatient_room() {
		return patient_room;
	}
	public void setPatient_room(String patient_room) {
		this.patient_room = patient_room;
	}
	public String getBpm() {
		return bpm;
	}
	public void setBpm(String bpm) {
		this.bpm = bpm;
	}
	public String getBody_temp() {
		return body_temp;
	}
	public void setBody_temp(String body_temp) {
		this.body_temp = body_temp;
	}
	public String getRespiration() {
		return respiration;
	}
	public void setRespiration(String respiration) {
		this.respiration = respiration;
	}
	public String getBlood_pressure() {
		return blood_pressure;
	}
	public void setBlood_pressure(String blood_pressure) {
		this.blood_pressure = blood_pressure;
	}
	public String getThe_others() {
		return the_others;
	}
	public void setThe_others(String the_others) {
		this.the_others = the_others;
	}
	public int getTeam() {
		return team;
	}
	public void setTeam(int team) {
		this.team = team;
	}
	
}
